import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
            //1. Scanner
            Scanner sc = new Scanner(System.in);
            //2. a, b 입력받기 : nextInt()
            int a = sc.nextInt();
            int b = sc.nextInt();
            //3. a, b 출력 : Syso
            System.out.println("a = " + a);
            System.out.println("b = " + b);
    }//main() 종료
}